#include "libft.h"

void	del_strs(char ***strs)
{
	int	i;

	i = 0;
	while (strs[0][i] != '\0')
	{
		ft_strdel(&(strs[0][i]));
		i++;
	}
	free(strs[0]);
	strs[0] = NULL;
}
